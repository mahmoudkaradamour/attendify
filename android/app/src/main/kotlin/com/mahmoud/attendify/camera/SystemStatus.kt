package com.mahmoud.attendify.camera

/**
 * SystemStatus
 *
 * Arabic:
 * هذا الـ enum يمثل جميع الحالات التي يمكن أن
 * يرسلها Native Layer إلى Flutter.
 *
 * الهدف منه:
 * - توحيد الرسائل والحالات
 * - فصل منطق النظام عن واجهة المستخدم
 * - تمكين Flutter من اتخاذ قرارات UX صحيحة
 *
 * Important:
 * - Flutter يجب أن يبني كل واجهاته اعتمادًا على هذه الحالات فقط
 * - لا يجب على Flutter تفسير أي منطق داخلي (ML / Threshold / Policy)
 */
enum class SystemStatus {

    // =========================================================
    // ✅ GENERAL / SYSTEM STATES
    // =========================================================

    /**
     * Arabic:
     * الحالة السليمة النهائية.
     *
     * تُستخدم عندما:
     * - تم كشف الوجه
     * - تم استخراج embedding بنجاح
     * - تمت المطابقة ونجحت
     *
     * Flutter:
     * - تسجيل الحضور
     * - عرض رسالة نجاح (إن لزم)
     */
    OK,

    /**
     * Arabic:
     * لم يتم منح صلاحية الكاميرا.
     *
     * Flutter:
     * - طلب الإذن من المستخدم
     * - أو توجيهه للإعدادات
     */
    NO_CAMERA_PERMISSION,

    /**
     * Arabic:
     * الكاميرا مشغولة أو غير متاحة:
     * - تطبيق آخر يستخدم الكاميرا
     * - قيود نظام (خصوصًا MIUI)
     *
     * Flutter:
     * - عرض رسالة "الكاميرا غير متاحة حاليًا"
     * - زر إعادة المحاولة
     */
    CAMERA_BUSY,

    /**
     * Arabic:
     * تم إغلاق الكاميرا فجأة:
     * - التطبيق دخل الخلفية
     * - الشاشة أُغلقت
     *
     * Flutter:
     * - إيقاف مؤقت
     * - إعادة التشغيل عند العودة
     */
    CAMERA_CLOSED,

    /**
     * Arabic:
     * ضغط ذاكرة مرتفع.
     *
     * Flutter:
     * - تقليل النشاط
     * - أو إعلام المستخدم
     */
    LOW_MEMORY,

    /**
     * Arabic:
     * خطأ داخلي غير متوقع.
     *
     * Flutter:
     * - رسالة خطأ عامة
     * - إمكانية إعادة المحاولة
     */
    INTERNAL_ERROR,

    // =========================================================
    // ✅ IMAGE QUALITY (LIVE CAMERA)
    // =========================================================

    /**
     * Arabic:
     * الإضاءة ضعيفة جدًا.
     *
     * Flutter:
     * - "الرجاء الانتقال إلى إضاءة أفضل"
     */
    IMAGE_TOO_DARK,

    /**
     * Arabic:
     * الإضاءة شديدة (تشبع).
     *
     * Flutter:
     * - "الرجاء الابتعاد عن مصدر الإضاءة"
     */
    IMAGE_TOO_BRIGHT,

    /**
     * Arabic:
     * الصورة ضبابية:
     * - حركة
     * - تركيز سيئ
     *
     * Flutter:
     * - "ثبت الهاتف وحاول مرة أخرى"
     */
    IMAGE_BLURRY,

    /**
     * Arabic:
     * تباين ضعيف جدًا:
     * - عدسة متسخة
     * - ضباب
     *
     * Flutter:
     * - "نظف العدسة أو غيّر المكان"
     */
    IMAGE_LOW_CONTRAST,

    // =========================================================
    // ✅ FACE DETECTION / MATCHING RESULTS
    // =========================================================

    /**
     * Arabic:
     * لم يتم تحقيق تطابق كافٍ
     * بين الوجه الحي والصورة المرجعية.
     *
     * Important:
     * - هذا لا يعني أن الصورة الحية سيئة
     * - بل يعني أن التشابه أقل من threshold المحدد
     *
     * Flutter:
     * - "لم يتم التحقق من الهوية"
     */
    FACE_NOT_MATCHED,

    /**
     * Arabic:
     * الصورة المرجعية غير معتمدة الجودة.
     *
     * السبب:
     * - فشلت عند الإدراج
     * - أو سياسة الإدارة تفرض منع المطابقة
     *
     * Flutter:
     * - "الرجاء التواصل مع الإدارة لتحديث الصورة"
     */
    REFERENCE_IMAGE_INVALID,

    /**
     * Arabic:
     * تم منع تسجيل الحضور
     * بسبب قرار إداري (Policy).
     *
     * Examples:
     * - الإدارة ألغت التحقق في هذا الوقت
     * - شرط أمني مؤقت
     *
     * Flutter:
     * - "تم إيقاف الحضور حسب سياسة الشركة"
     */
    ATTENDANCE_BLOCKED_BY_POLICY
}